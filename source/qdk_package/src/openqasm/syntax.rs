//! Rich, read-only Python projections of the *syntactic* `OpenQASM` AST.
//!
//! Where [`super::semantic`] projects the resolved semantic tree, this module
//! projects the raw parse tree produced by [`qdk_openqasm::parse_source`]. The
//! syntactic tree carries no resolved type, const-value, or symbol information,
//! so its nodes extend [`Expression`] / [`Statement`] directly (a three-level
//! `QASMNode -> Expression|Statement -> concrete` chain) rather than through the
//! semantic `SemExpr` / `SemStmt` bases.
//!
//! Concrete per-variant leaf classes are generated by the [`qasm_node!`] macro
//! (`@sexpr` / `@sstmt` categories) and expose *named*, typed accessors for
//! their children (for example `BinaryExpression.lhs` / `.rhs` / `.op`) plus a
//! uniform `children()` method for generic traversal. There is intentionally no
//! `kind` discriminant; callers dispatch via `isinstance` and
//! `type(node).__name__`.
//!
//! Class names follow the [`openqasm3` reference AST][openqasm3] wherever an
//! equivalent class exists (for example `BinaryExpression`, `QuantumGate`,
//! `ClassicalDeclaration`, `ForInLoop`, `Box`, `SwitchStatement`). Variants with
//! no `openqasm3` equivalent take a descriptive QDK name (for example
//! `ErrorExpression`, `ErrorStatement`, `ParenExpression`, `CompoundAssignment`).
//!
//! All projections are eagerly materialized into owned, frozen values (scalars
//! plus `Py<PyAny>` references to already-built children), so they are
//! `Send + Sync` and hold no borrow into the Rust parse result.
//!
//! [openqasm3]: https://github.com/openqasm/openqasm/tree/main/source/openqasm
//!
//! ## Syntactic name to variant table
//!
//! The mapping below is the single source of truth for the Python class names
//! and the syntactic AST variants they project. It is kept in sync with
//! `qdk_openqasm::parser::ast`.
//!
//! Statements (`StmtKind` -> class):
//! * `Alias` -> `AliasStatement`
//! * `Assign` -> `ClassicalAssignment`
//! * `AssignOp` -> `CompoundAssignment`
//! * `Barrier` -> `QuantumBarrier`
//! * `Box` -> `Box`
//! * `Break` -> `BreakStatement`
//! * `Block` -> `CompoundStatement`
//! * `Cal` -> `CalibrationStatement`
//! * `CalibrationGrammar` -> `CalibrationGrammarDeclaration`
//! * `ClassicalDecl` -> `ClassicalDeclaration`
//! * `ConstDecl` -> `ConstantDeclaration`
//! * `Continue` -> `ContinueStatement`
//! * `Def` -> `SubroutineDefinition`
//! * `DefCal` -> `CalibrationDefinition`
//! * `Delay` -> `DelayInstruction`
//! * `End` -> `EndStatement`
//! * `ExprStmt` -> `ExpressionStatement`
//! * `ExternDecl` -> `ExternDeclaration`
//! * `For` -> `ForInLoop`
//! * `If` -> `BranchingStatement`
//! * `GateCall` -> `QuantumGate`
//! * `GPhase` -> `QuantumPhase`
//! * `Include` -> `Include`
//! * `IODeclaration` -> `IODeclaration`
//! * `Measure` -> `QuantumMeasurementStatement`
//! * `Pragma` -> `Pragma`
//! * `QuantumGateDefinition` -> `QuantumGateDefinition`
//! * `QuantumDecl` -> `QubitDeclaration`
//! * `Reset` -> `QuantumReset`
//! * `Return` -> `ReturnStatement`
//! * `Switch` -> `SwitchStatement`
//! * `WhileLoop` -> `WhileLoop`
//! * `Err` -> `ErrorStatement`
//!
//! Expressions (`ExprKind` -> class):
//! * `Err` -> `ErrorExpression`
//! * `Ident` -> `Identifier`
//! * `UnaryOp` -> `UnaryExpression`
//! * `BinaryOp` -> `BinaryExpression`
//! * `Lit` -> `LiteralExpression`
//! * `FunctionCall` -> `FunctionCall`
//! * `Cast` -> `Cast`
//! * `IndexExpr` -> `IndexExpression`
//! * `Paren` -> `ParenExpression`
//! * `DurationOf` -> `DurationOf`
//!
//! Auxiliary syntactic nodes with no direct `ExprKind` / `StmtKind` variant:
//! * An indexed identifier (`a[i]` in an l-value position) -> `IndexedIdentifier`
//! * A hardware-qubit gate operand (`$0`) -> `HardwareQubit`
//! * A concatenation r-value (`a ++ b`) -> `Concatenation`
//! * A measurement r-value (`measure q`) -> `QuantumMeasurement`
//! * A gate modifier (`ctrl @`, `pow(2) @`) -> `QuantumGateModifier`

