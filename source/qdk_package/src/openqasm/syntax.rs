/// The root of a parsed `OpenQASM` program.
#[pyclass(extends = QASMNode, frozen, module = "qdk._native")]
pub(crate) struct Program {
    statements: Vec<Py<PyAny>>,
    version: Option<String>,
    document: Py<SourceDocument>,
}

impl Program {
    pub(crate) fn source_document(&self, py: Python<'_>) -> Py<SourceDocument> {
        self.document.clone_ref(py)
    }
}

#[pymethods]
impl Program {
    /// The top-level statements of the program, in source order.
    #[getter]
    fn statements(&self, py: Python<'_>) -> Vec<Py<PyAny>> {
        self.statements
            .iter()
            .map(|stmt| stmt.clone_ref(py))
            .collect()
    }

    /// The declared `OpenQASM` version, if any (for example `"3.0"`).
    #[getter]
    fn version(&self) -> Option<String> {
        self.version.clone()
    }

    /// The immutable source document for this parse snapshot.
    #[getter]
    fn document(&self, py: Python<'_>) -> Py<SourceDocument> {
        self.document.clone_ref(py)
    }

    /// The program's child nodes: its top-level statements.
    fn children(&self, py: Python<'_>) -> Vec<Py<PyAny>> {
        self.statements
            .iter()
            .map(|stmt| stmt.clone_ref(py))
            .collect()
    }

    fn __repr__(&self) -> String {
        format!(
            "Program(statements=[{} items], version={:?})",
            self.statements.len(),
            self.version
        )
    }
}

/// A quantum gate modifier (for example `ctrl @`, `inv @`, or `pow(2) @`).
///
/// This extends [`QASMNode`] directly, mirroring the `openqasm3`
/// `QuantumGateModifier` node, which is neither an expression nor a statement.
#[pyclass(extends = QASMNode, frozen, module = "qdk._native")]
pub(crate) struct QuantumGateModifier {
    /// The modifier keyword: `"inv"`, `"pow"`, `"ctrl"`, or `"negctrl"`.
    #[pyo3(get)]
    modifier: String,
    /// The modifier's argument expression, if any (the exponent for `pow` or the
    /// optional control count for `ctrl` / `negctrl`).
    #[pyo3(get)]
    argument: Option<Py<PyAny>>,
}

#[pymethods]
impl QuantumGateModifier {
    /// The modifier's child nodes: its argument expression, if present.
    fn children(&self, py: Python<'_>) -> Vec<Py<PyAny>> {
        match &self.argument {
            Some(argument) => vec![argument.clone_ref(py)],
            None => Vec::new(),
        }
    }

    fn __repr__(&self) -> String {
        format!("QuantumGateModifier(modifier={:?})", self.modifier)
    }
}

impl QuantumGateModifier {
    fn build(
        py: Python<'_>,
        span: Span,
        modifier: String,
        argument: Option<Py<PyAny>>,
    ) -> PyResult<Py<PyAny>> {
        let init = PyClassInitializer::from(QASMNode { span })
            .add_subclass(QuantumGateModifier { modifier, argument });
        Ok(Py::new(py, init)?.into_any())
    }
}

