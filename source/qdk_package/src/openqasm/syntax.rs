/// The projected value, element children, and kind tag of a literal expression.
///
/// Scalar literals carry a Python value and no elements; array literals carry no
/// scalar value and expose their element expressions as children. The `kind` tag
/// names the underlying [`ast::LiteralKind`] variant.
type LiteralParts = (Option<Py<PyAny>>, Vec<Py<PyAny>>, String);

fn literal_parts(py: Python<'_>, lit: &ast::LiteralKind) -> PyResult<LiteralParts> {
    use ast::LiteralKind as Kind;
    let parts = match lit {
        Kind::Array(exprs) => {
            let mut elements = Vec::with_capacity(exprs.len());
            for expr in exprs {
                elements.push(build_expr(py, expr)?);
            }
            (None, elements, "Array".to_string())
        }
        Kind::Bool(value) => (
            Some((*value).into_py_any(py)?),
            Vec::new(),
            "Bool".to_string(),
        ),
        Kind::Int(value) => (
            Some((*value).into_py_any(py)?),
            Vec::new(),
            "Int".to_string(),
        ),
        Kind::Float(value) => (
            Some((*value).into_py_any(py)?),
            Vec::new(),
            "Float".to_string(),
        ),
        Kind::Imaginary(value) => (
            Some(PyComplex::from_doubles(py, 0.0, *value).into_any().unbind()),
            Vec::new(),
            "Imaginary".to_string(),
        ),
        Kind::String(value) => (
            Some(value.to_string().into_py_any(py)?),
            Vec::new(),
            "String".to_string(),
        ),
        Kind::Duration(value, unit) => (
            Some(format!("{value}{unit}").into_py_any(py)?),
            Vec::new(),
            "Duration".to_string(),
        ),
        Kind::Bitstring(value, width) => {
            let width = *width as usize;
            (
                Some(format!("{:0>width$}", value.to_str_radix(2)).into_py_any(py)?),
                Vec::new(),
                "Bitstring".to_string(),
            )
        }
        Kind::BigInt(value) => (
            Some(bigint_to_py(py, &value.to_str_radix(10))?),
            Vec::new(),
            "BigInt".to_string(),
        ),
    };
    Ok(parts)
}

fn bigint_to_py(py: Python<'_>, decimal: &str) -> PyResult<Py<PyAny>> {
    Ok(py
        .import("builtins")?
        .getattr("int")?
        .call1((decimal,))?
        .unbind())
}
