/// Builds the Python [`Program`] root from a parsed syntax program.
///
/// When `program` is `None` (for example when the source failed to parse into a
/// tree at all), an empty program is returned so callers still receive a
/// well-formed root alongside the diagnostics.
pub(crate) fn build_program(
    py: Python<'_>,
    program: Option<&SyntaxProgram>,
    document: Py<SourceDocument>,
) -> PyResult<Py<Program>> {
    let (span, statements, version) = match program {
        Some(program) => {
            let mut statements = Vec::with_capacity(program.statements.len());
            for stmt in &program.statements {
                statements.push(build_stmt(py, stmt)?);
            }
            let version = program.version.as_ref().map(ToString::to_string);
            (Span::from(program.span), statements, version)
        }
        None => (Span { lo: 0, hi: 0 }, Vec::new(), None),
    };

    let init = PyClassInitializer::from(QASMNode { span }).add_subclass(Program {
        statements,
        version,
        document,
    });
    Py::new(py, init)
}

#[allow(clippy::too_many_lines)]
fn build_stmt(py: Python<'_>, stmt: &ast::Stmt) -> PyResult<Py<PyAny>> {
    let span = Span::from(stmt.span);
    let annotations = build_annotations(py, &stmt.annotations)?;
    let obj: Py<PyAny> = match stmt.kind.as_ref() {
        ast::StmtKind::Alias(s) => {
            let target = build_ident_or_indexed(py, &s.ident)?;
            let exprs = expr_list(py, &s.exprs)?;
            Py::new(py, AliasStatement::init(span, annotations, target, exprs))?.into_any()
        }
        ast::StmtKind::Assign(s) => {
            let lhs = build_ident_or_indexed(py, &s.lhs)?;
            let rhs = build_value_expr(py, &s.rhs)?;
            Py::new(py, ClassicalAssignment::init(span, annotations, lhs, rhs))?.into_any()
        }
        ast::StmtKind::AssignOp(s) => {
            let lhs = build_ident_or_indexed(py, &s.lhs)?;
            let rhs = build_value_expr(py, &s.rhs)?;
            Py::new(
                py,
                CompoundAssignment::init(span, annotations, s.op.to_string(), lhs, rhs),
            )?
            .into_any()
        }
        ast::StmtKind::Barrier(s) => {
            let qubits = gate_operand_list(py, &s.qubits)?;
            Py::new(py, QuantumBarrier::init(span, annotations, qubits))?.into_any()
        }
        ast::StmtKind::Box(s) => {
            let duration = opt_expr(py, s.duration.as_ref())?;
            let body = stmt_list(py, &s.body)?;
            Py::new(py, Box::init(span, annotations, duration, body))?.into_any()
        }
        ast::StmtKind::Break(_) => Py::new(py, BreakStatement::init(span, annotations))?.into_any(),
        ast::StmtKind::Block(s) => {
            let statements = stmt_list(py, &s.stmts)?;
            Py::new(py, CompoundStatement::init(span, annotations, statements))?.into_any()
        }
        ast::StmtKind::Cal(s) => Py::new(
            py,
            CalibrationStatement::init(span, annotations, s.content.to_string()),
        )?
        .into_any(),
        ast::StmtKind::CalibrationGrammar(s) => Py::new(
            py,
            CalibrationGrammarDeclaration::init(span, annotations, s.name.to_string()),
        )?
        .into_any(),
        ast::StmtKind::ClassicalDecl(s) => {
            let ty = type_definition_parts(py, &s.ty)?;
            let identifier = build_identifier(py, &s.identifier)?;
            let init_expr = match s.init_expr.as_deref() {
                Some(init) => Some(build_value_expr(py, init)?),
                None => None,
            };
            Py::new(
                py,
                ClassicalDeclaration::init(
                    span,
                    annotations,
                    ty.name,
                    ty.expressions,
                    identifier,
                    init_expr,
                ),
            )?
            .into_any()
        }
        ast::StmtKind::ConstDecl(s) => {
            let ty = type_definition_parts(py, &s.ty)?;
            let identifier = build_identifier(py, &s.identifier)?;
            let init_expr = build_value_expr(py, &s.init_expr)?;
            Py::new(
                py,
                ConstantDeclaration::init(
                    span,
                    annotations,
                    ty.name,
                    ty.expressions,
                    identifier,
                    init_expr,
                ),
            )?
            .into_any()
        }
        ast::StmtKind::Continue(_) => {
            Py::new(py, ContinueStatement::init(span, annotations))?.into_any()
        }
        ast::StmtKind::Def(s) => {
            let name = build_identifier(py, &s.name)?;
            let mut params = Vec::with_capacity(s.params.len());
            for param in &s.params {
                let identifier = build_identifier(py, &param.ident)?;
                let (type_name, type_expressions) =
                    def_parameter_type_parts(py, param.ty.as_ref())?;
                params.push(
                    Py::new(
                        py,
                        SubroutineParameter::init(
                            Span::from(param.span),
                            identifier,
                            type_name,
                            type_expressions,
                        ),
                    )?
                    .into_any(),
                );
            }
            let return_ty = s
                .return_type
                .as_ref()
                .map(|ty| scalar_type_parts(py, ty))
                .transpose()?;
            let (return_type_name, return_type_expressions) = return_ty
                .map(|ty| (Some(ty.name), ty.expressions))
                .unwrap_or_default();
            let body = stmt_list(py, &s.body.stmts)?;
            Py::new(
                py,
                SubroutineDefinition::init(
                    span,
                    annotations,
                    name,
                    params,
                    return_type_name,
                    return_type_expressions,
                    body,
                ),
            )?
            .into_any()
        }
        ast::StmtKind::DefCal(s) => Py::new(
            py,
            CalibrationDefinition::init(span, annotations, s.content.to_string()),
        )?
        .into_any(),
        ast::StmtKind::Delay(s) => {
            let duration = build_expr(py, &s.duration)?;
            let qubits = gate_operand_list(py, &s.qubits)?;
            Py::new(
                py,
                DelayInstruction::init(span, annotations, duration, qubits),
            )?
            .into_any()
        }
        ast::StmtKind::End(_) => Py::new(py, EndStatement::init(span, annotations))?.into_any(),
        ast::StmtKind::ExprStmt(s) => {
            let expr = build_expr(py, &s.expr)?;
            Py::new(py, ExpressionStatement::init(span, annotations, expr))?.into_any()
        }
        ast::StmtKind::ExternDecl(s) => {
            let name = build_identifier(py, &s.ident)?;
            let mut param_type_names = Vec::with_capacity(s.params.len());
            let mut param_type_expressions = Vec::new();
            for param in &s.params {
                let ty = extern_parameter_type_parts(py, param)?;
                param_type_names.push(ty.name);
                param_type_expressions.extend(ty.expressions);
            }
            let return_ty = s
                .return_type
                .as_ref()
                .map(|ty| scalar_type_parts(py, ty))
                .transpose()?;
            let (return_type_name, return_type_expressions) = return_ty
                .map(|ty| (Some(ty.name), ty.expressions))
                .unwrap_or_default();
            Py::new(
                py,
                ExternDeclaration::init(
                    span,
                    annotations,
                    name,
                    param_type_names,
                    param_type_expressions,
                    return_type_name,
                    return_type_expressions,
                ),
            )?
            .into_any()
        }
        ast::StmtKind::For(s) => {
            let ty = scalar_type_parts(py, &s.ty)?;
            let identifier = build_identifier(py, &s.ident)?;
            let iterable = build_enumerable_set(py, &s.set_declaration)?;
            let body = build_stmt(py, &s.body)?;
            Py::new(
                py,
                ForInLoop::init(
                    span,
                    annotations,
                    ty.name,
                    ty.expressions,
                    identifier,
                    iterable,
                    body,
                ),
            )?
            .into_any()
        }
        ast::StmtKind::If(s) => {
            let condition = build_expr(py, &s.condition)?;
            let if_body = build_stmt(py, &s.if_body)?;
            let else_body = match &s.else_body {
                Some(else_body) => Some(build_stmt(py, else_body)?),
                None => None,
            };
            Py::new(
                py,
                BranchingStatement::init(span, annotations, condition, if_body, else_body),
            )?
            .into_any()
        }
        ast::StmtKind::GateCall(s) => {
            let name = build_identifier(py, &s.name)?;
            let modifiers = modifier_list(py, &s.modifiers)?;
            let args = expr_list(py, &s.args)?;
            let qubits = gate_operand_list(py, &s.qubits)?;
            let duration = opt_expr(py, s.duration.as_ref())?;
            Py::new(
                py,
                QuantumGate::init(span, annotations, name, modifiers, args, qubits, duration),
            )?
            .into_any()
        }
        ast::StmtKind::GPhase(s) => {
            let modifiers = modifier_list(py, &s.modifiers)?;
            let args = expr_list(py, &s.args)?;
            let qubits = gate_operand_list(py, &s.qubits)?;
            let duration = opt_expr(py, s.duration.as_ref())?;
            Py::new(
                py,
                QuantumPhase::init(span, annotations, modifiers, args, qubits, duration),
            )?
            .into_any()
        }
        ast::StmtKind::Include(s) => {
            Py::new(py, Include::init(span, annotations, s.filename.to_string()))?.into_any()
        }
        ast::StmtKind::IODeclaration(s) => {
            let ty = type_definition_parts(py, &s.ty)?;
            let identifier = build_identifier(py, &s.ident)?;
            Py::new(
                py,
                IODeclaration::init(
                    span,
                    annotations,
                    s.io_identifier.to_string(),
                    ty.name,
                    ty.expressions,
                    identifier,
                ),
            )?
            .into_any()
        }
        ast::StmtKind::Measure(s) => {
            let mut qubits = Vec::new();
            collect_gate_operand(py, &mut qubits, &s.measurement.operand)?;
            let target = match &s.target {
                Some(target) => Some(build_ident_or_indexed(py, target)?),
                None => None,
            };
            Py::new(
                py,
                QuantumMeasurementStatement::init(span, annotations, qubits, target),
            )?
            .into_any()
        }
        ast::StmtKind::Pragma(s) => {
            let name = s.identifier.as_ref().map(PathKind::as_string);
            let value = s.value.as_ref().map(ToString::to_string);
            Py::new(py, Pragma::init(span, annotations, name, value))?.into_any()
        }
        ast::StmtKind::QuantumGateDefinition(s) => {
            let name = build_identifier(py, &s.ident)?;
            let params = s
                .params
                .iter()
                .filter_map(|item| item.item_as_ref().map(|ident| ident.name.to_string()))
                .collect();
            let qubits = s
                .qubits
                .iter()
                .filter_map(|item| item.item_as_ref().map(|ident| ident.name.to_string()))
                .collect();
            let body = stmt_list(py, &s.body.stmts)?;
            Py::new(
                py,
                QuantumGateDefinition::init(span, annotations, name, params, qubits, body),
            )?
            .into_any()
        }
        ast::StmtKind::QuantumDecl(s) => {
            let qubit = build_identifier(py, &s.qubit)?;
            let size = opt_expr(py, s.ty.size.as_ref())?;
            Py::new(py, QubitDeclaration::init(span, annotations, qubit, size))?.into_any()
        }
        ast::StmtKind::Reset(s) => {
            let mut qubits = Vec::new();
            collect_gate_operand(py, &mut qubits, &s.operand)?;
            Py::new(py, QuantumReset::init(span, annotations, qubits))?.into_any()
        }
        ast::StmtKind::Return(s) => {
            let value = match s.expr.as_deref() {
                Some(expr) => Some(build_value_expr(py, expr)?),
                None => None,
            };
            Py::new(py, ReturnStatement::init(span, annotations, value))?.into_any()
        }
        ast::StmtKind::Switch(s) => {
            let target = build_expr(py, &s.target)?;
            let mut cases = Vec::with_capacity(s.cases.len());
            for case in &s.cases {
                let labels = expr_list(py, &case.labels)?;
                let body = stmt_list(py, &case.block.stmts)?;
                cases.push(
                    Py::new(py, SwitchCase::init(Span::from(case.span), labels, body))?.into_any(),
                );
            }
            let default = s
                .default
                .as_ref()
                .map(|block| stmt_list(py, &block.stmts))
                .transpose()?;
            Py::new(
                py,
                SwitchStatement::init(span, annotations, target, cases, default),
            )?
            .into_any()
        }
        ast::StmtKind::WhileLoop(s) => {
            let condition = build_expr(py, &s.while_condition)?;
            let body = build_stmt(py, &s.body)?;
            Py::new(py, WhileLoop::init(span, annotations, condition, body))?.into_any()
        }
        ast::StmtKind::Err => Py::new(py, ErrorStatement::init(span, annotations))?.into_any(),
    };
    Ok(obj)
}

fn build_annotations(
    py: Python<'_>,
    annotations: &[ast::Annotation],
) -> PyResult<Vec<Py<Annotation>>> {
    annotations
        .iter()
        .map(|annotation| {
            Py::new(
                py,
                Annotation::init(
                    Span::from(annotation.span),
                    annotation.identifier.as_string(),
                    annotation.value.as_ref().map(ToString::to_string),
                    annotation.value_span.map(Span::from),
                ),
            )
        })
        .collect()
}

fn build_expr(py: Python<'_>, expr: &ast::Expr) -> PyResult<Py<PyAny>> {
    let span = Span::from(expr.span);
    let obj: Py<PyAny> = match expr.kind.as_ref() {
        ast::ExprKind::Err => Py::new(py, ErrorExpression::init(span))?.into_any(),
        ast::ExprKind::Ident(ident) => build_identifier(py, ident)?,
        ast::ExprKind::UnaryOp(e) => {
            let operand = build_expr(py, &e.expr)?;
            Py::new(py, UnaryExpression::init(span, e.op.to_string(), operand))?.into_any()
        }
        ast::ExprKind::BinaryOp(e) => {
            let lhs = build_expr(py, &e.lhs)?;
            let rhs = build_expr(py, &e.rhs)?;
            Py::new(py, BinaryExpression::init(span, e.op.to_string(), lhs, rhs))?.into_any()
        }
        ast::ExprKind::Lit(lit) => {
            let (value, elements, kind) = literal_parts(py, &lit.kind)?;
            Py::new(py, LiteralExpression::init(span, value, elements, kind))?.into_any()
        }
        ast::ExprKind::FunctionCall(e) => {
            let name = build_identifier(py, &e.name)?;
            let args = expr_list(py, &e.args)?;
            Py::new(py, FunctionCall::init(span, name, args))?.into_any()
        }
        ast::ExprKind::Cast(e) => {
            let ty = type_definition_parts(py, &e.ty)?;
            let operand = build_expr(py, &e.arg)?;
            Py::new(py, Cast::init(span, ty.name, ty.expressions, operand))?.into_any()
        }
        ast::ExprKind::IndexExpr(e) => {
            let collection = build_expr(py, &e.collection)?;
            let indices = vec![build_index(py, &e.index)?];
            Py::new(py, IndexExpression::init(span, collection, indices))?.into_any()
        }
        ast::ExprKind::Paren(inner) => {
            let operand = build_expr(py, inner)?;
            Py::new(py, ParenExpression::init(span, operand))?.into_any()
        }
        ast::ExprKind::DurationOf(d) => {
            let body = stmt_list(py, &d.scope.stmts)?;
            Py::new(py, DurationOf::init(span, body))?.into_any()
        }
    };
    Ok(obj)
}

/// Builds an [`Identifier`] expression node as an owned `Py<PyAny>`.
fn build_identifier(py: Python<'_>, ident: &ast::Ident) -> PyResult<Py<PyAny>> {
    let init = Identifier::init(Span::from(ident.span), ident.name.to_string());
    Ok(Py::new(py, init)?.into_any())
}

/// Builds either an [`Identifier`] or an [`IndexedIdentifier`] node.
fn build_ident_or_indexed(py: Python<'_>, ident: &ast::IdentOrIndexedIdent) -> PyResult<Py<PyAny>> {
    match ident {
        ast::IdentOrIndexedIdent::Ident(ident) => build_identifier(py, ident),
        ast::IdentOrIndexedIdent::IndexedIdent(indexed) => {
            let name = build_identifier(py, &indexed.ident)?;
            let mut indices = Vec::with_capacity(indexed.indices.len());
            for index in &indexed.indices {
                indices.push(build_index(py, index)?);
            }
            let init = IndexedIdentifier::init(Span::from(indexed.span), name, indices);
            Ok(Py::new(py, init)?.into_any())
        }
    }
}

/// Builds the r-value of an alias, declaration, assignment, or return statement.
fn build_value_expr(py: Python<'_>, value: &ast::ValueExpr) -> PyResult<Py<PyAny>> {
    match value {
        ast::ValueExpr::Expr(expr) => build_expr(py, expr),
        ast::ValueExpr::Concat(concat) => {
            let operands = expr_list(py, &concat.operands)?;
            let init = Concatenation::init(Span::from(concat.span), operands);
            Ok(Py::new(py, init)?.into_any())
        }
        ast::ValueExpr::Measurement(measure) => {
            let mut qubits = Vec::new();
            collect_gate_operand(py, &mut qubits, &measure.operand)?;
            let init = QuantumMeasurement::init(Span::from(measure.span), qubits);
            Ok(Py::new(py, init)?.into_any())
        }
    }
}

/// Builds a [`HardwareQubit`] gate-operand node as an owned `Py<PyAny>`.
fn build_hardware_qubit(py: Python<'_>, hw: &ast::HardwareQubit) -> PyResult<Py<PyAny>> {
    let init = HardwareQubit::init(Span::from(hw.span), hw.name.to_string());
    Ok(Py::new(py, init)?.into_any())
}

