/// The projected value and element children of a literal expression.
///
/// Scalar literals carry a Python value and no elements; array literals carry no
/// scalar value and expose their element expressions as children.
type LiteralParts = (Option<Py<PyAny>>, Vec<Py<PyAny>>);

fn literal_expr_parts(
    py: Python<'_>,
    lit: &LiteralKind,
    symbols: &SymbolTable,
) -> PyResult<LiteralParts> {
    match lit {
        LiteralKind::Array(array) => {
            let mut elements = Vec::with_capacity(array.data.len());
            for item in &array.data {
                elements.push(build_expr(py, item, symbols)?);
            }
            Ok((None, elements))
        }
        other => Ok((literal_kind_value(py, other)?, Vec::new())),
    }
}

fn literal_value(py: Python<'_>, lit: Option<&LiteralKind>) -> PyResult<Option<Py<PyAny>>> {
    match lit {
        Some(lit) => literal_kind_value(py, lit),
        None => Ok(None),
    }
}

fn literal_kind_value(py: Python<'_>, lit: &LiteralKind) -> PyResult<Option<Py<PyAny>>> {
    let value: Py<PyAny> = match lit {
        LiteralKind::Array(_) => return Ok(None),
        LiteralKind::Bool(b) | LiteralKind::Bit(b) => (*b).into_py_any(py)?,
        LiteralKind::Int(i) => (*i).into_py_any(py)?,
        LiteralKind::Float(f) => (*f).into_py_any(py)?,
        LiteralKind::Complex(value) => PyComplex::from_doubles(py, value.real, value.imag)
            .into_any()
            .unbind(),
        LiteralKind::Angle(angle) => angle.to_string().into_py_any(py)?,
        LiteralKind::Duration(duration) => duration.to_string().into_py_any(py)?,
        LiteralKind::Bitstring(value, width) => {
            let width = *width as usize;
            format!("{:0>width$}", value.to_str_radix(2)).into_py_any(py)?
        }
        LiteralKind::BigInt(value) => bigint_to_py(py, &value.to_str_radix(10))?,
    };
    Ok(Some(value))
}

fn bigint_to_py(py: Python<'_>, decimal: &str) -> PyResult<Py<PyAny>> {
    Ok(py
        .import("builtins")?
        .getattr("int")?
        .call1((decimal,))?
        .unbind())
}
