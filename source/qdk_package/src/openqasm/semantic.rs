//! Rich, read-only Python projections of the semantic `OpenQASM` AST.
//!
//! Where [`super::nodes`] projects the *syntactic* tree, this module projects
//! the *semantic* tree produced by [`qdk_openqasm::analyze_source`]. The
//! semantic tree carries resolved type and const-value information. Each class
//! keeps a `Sem`-prefixed Rust identifier but is presented to Python under a
//! clean, un-prefixed name inside the `qdk._native._semantic` submodule (for
//! example Rust `SemGateCall` -> Python `QuantumGate`). Isolating the family in
//! a submodule lets it use the un-prefixed names without colliding with the
//! syntax layer's `openqasm3`-parity names in the flat `qdk._native` module.
//!
//! * [`SemExpr`] (Python `SemanticExpression`) is the base of every semantic
//!   expression. It extends [`Expression`] (and therefore `QASMNode`) and adds
//!   `ty`, `const_value`, and `symbol` accessors.
//! * [`SemStmt`] (Python `SemanticStatement`) is the base of every semantic
//!   statement. It extends [`Statement`] (and therefore `QASMNode`) and adds an
//!   `annotations` accessor.
//! * [`SemProgram`] (Python `Program`) is the root of a semantic program.
//!
//! Concrete per-variant leaf classes are generated by the [`qasm_node!`] macro
//! and expose *named*, typed accessors for their children (for example
//! `SemBinaryOpExpr.lhs` / `.rhs` / `.op`, presented to Python as
//! `BinaryExpression`) plus a uniform `children()` method for generic
//! traversal. There is intentionally no `kind` discriminant; callers dispatch
//! via `isinstance` and `type(node).__name__`.
//!
//! The resolved [`SymbolTable`] is projected as [`SemSymbolTable`], an iterable
//! collection of [`SemSymbol`] views, and types are projected as opaque
//! [`SemType`] values.
//!
//! All projections are eagerly materialized into owned, frozen values (scalars
//! plus `Py<PyAny>` references to already-built children), so they are
//! `Send + Sync` and hold no borrow into the Rust analysis result.
//!
//! ## Semantic name to variant table
//!
//! The mapping below is the single source of truth for the semantic AST
//! variants, their `Sem`-prefixed Rust identifiers, and the clean Python names
//! they present in the `qdk._native._semantic` submodule. It is kept in sync
//! with `qdk_openqasm::semantic::ast`.
//!
//! Statements (`StmtKind` -> Rust ident -> Python name):
//! * `Alias` -> `SemAliasDecl` -> `AliasStatement`
//! * `Assign` -> `SemAssign` -> `ClassicalAssignment`
//! * `Barrier` -> `SemBarrier` -> `QuantumBarrier`
//! * `Box` -> `SemBox` -> `Box`
//! * `Block` -> `SemBlock` -> `CompoundStatement`
//! * `Break` -> `SemBreak` -> `BreakStatement`
//! * `Calibration` -> `SemCalibration` -> `CalibrationStatement`
//! * `CalibrationGrammar` -> `SemCalibrationGrammar` -> `CalibrationGrammarDeclaration`
//! * `ClassicalDecl` -> `SemClassicalDecl` -> `ClassicalDeclaration`
//! * `Continue` -> `SemContinue` -> `ContinueStatement`
//! * `Def` -> `SemDef` -> `SubroutineDefinition`
//! * `DefCal` -> `SemDefCal` -> `CalibrationDefinition`
//! * `Delay` -> `SemDelay` -> `DelayInstruction`
//! * `End` -> `SemEnd` -> `EndStatement`
//! * `ExprStmt` -> `SemExprStmt` -> `ExpressionStatement`
//! * `ExternDecl` -> `SemExternDecl` -> `ExternDeclaration`
//! * `For` -> `SemForLoop` -> `ForInLoop`
//! * `GateCall` -> `SemGateCall` -> `QuantumGate`
//! * `If` -> `SemIfStmt` -> `BranchingStatement`
//! * `Include` -> `SemInclude` -> `Include`
//! * `IndexedAssign` -> `SemIndexedAssign` -> `IndexedClassicalAssignment`
//! * `InputDeclaration` -> `SemInputDeclaration` -> `InputDeclaration`
//! * `OutputDeclaration` -> `SemOutputDeclaration` -> `OutputDeclaration`
//! * `MeasureArrow` -> `SemMeasureArrow` -> `QuantumMeasurementStatement`
//! * `Pragma` -> `SemPragma` -> `Pragma`
//! * `QuantumGateDefinition` -> `SemGateDefinition` -> `QuantumGateDefinition`
//! * `QubitDecl` -> `SemQubitDecl` -> `QubitDeclaration`
//! * `QubitArrayDecl` -> `SemQubitArrayDecl` -> `QubitArrayDeclaration`
//! * `Reset` -> `SemReset` -> `QuantumReset`
//! * `Return` -> `SemReturn` -> `ReturnStatement`
//! * `Switch` -> `SemSwitch` -> `SwitchStatement`
//! * `WhileLoop` -> `SemWhileLoop` -> `WhileLoop`
//! * `Err` -> `SemErrStmt` -> `ErrorStatement`
//!
//! Expressions (`ExprKind` -> Rust ident -> Python name):
//! * `Err` -> `SemErrExpr` -> `ErrorExpression`
//! * `ResolvedIdent` -> `SemResolvedIdent` -> `Identifier`
//! * `CapturedResolvedIdent` -> `SemCapturedResolvedIdent` -> `CapturedIdentifier`
//! * `UnaryOp` -> `SemUnaryOpExpr` -> `UnaryExpression`
//! * `BinaryOp` -> `SemBinaryOpExpr` -> `BinaryExpression`
//! * `Lit` -> `SemLiteral` -> `LiteralExpression`
//! * `ResolvedFunctionCall` -> `SemFunctionCall` -> `FunctionCall`
//! * `BuiltinFunctionCall` -> `SemBuiltinFunctionCall` -> `BuiltinFunctionCall`
//! * `Cast` -> `SemCast` -> `Cast`
//! * `IndexedExpr` -> `SemIndexedExpr` -> `IndexExpression`
//! * `Paren` -> `SemParen` -> `ParenExpression`
//! * `Measure` -> `SemMeasure` -> `QuantumMeasurement`
//! * `RuntimeSizeof` -> `SemRuntimeSizeof` -> `RuntimeSizeof`
//! * `EvaluatedDurationof` -> `SemEvaluatedDurationof` -> `DurationOf`
//! * `Concat` -> `SemConcat` -> `Concatenation`
//!
//! The shared category bases and projections are `SemExpr` ->
//! `SemanticExpression`, `SemStmt` -> `SemanticStatement`, `SemProgram` ->
//! `Program`, `SemType` -> `Type`,
//! `SemSymbol` -> `Symbol`, and `SemSymbolTable` -> `SymbolTable`.
//!
//! Gate operands of the hardware-qubit form are projected as
//! [`SemHardwareQubit`] (Python `HardwareQubit`) so that no operand is silently
//! dropped.

