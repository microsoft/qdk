/// The root of a semantic `OpenQASM` program.
#[pyclass(extends = QASMNode, frozen, name = "Program", module = "qdk._native._semantic")]
pub(crate) struct SemProgram {
    /// The declared `OpenQASM` version, if any (for example `"3.0"`).
    #[pyo3(get)]
    version: Option<String>,
    /// The program's top-level pragmas, in source order.
    #[pyo3(get)]
    pragmas: Vec<Py<PyAny>>,
    /// The program's top-level statements, in source order.
    #[pyo3(get)]
    statements: Vec<Py<PyAny>>,
    document: Py<SourceDocument>,
}

#[pymethods]
impl SemProgram {
    /// The immutable source document for this analysis snapshot.
    #[getter]
    fn document(&self, py: Python<'_>) -> Py<SourceDocument> {
        self.document.clone_ref(py)
    }

    /// The program's child nodes: pragmas followed by statements.
    fn children(&self, py: Python<'_>) -> Vec<Py<PyAny>> {
        let mut out = Vec::with_capacity(self.pragmas.len() + self.statements.len());
        out.extend(self.pragmas.iter().map(|node| node.clone_ref(py)));
        out.extend(self.statements.iter().map(|node| node.clone_ref(py)));
        out
    }

    fn __repr__(&self) -> String {
        format!("Program(statements=[{} items])", self.statements.len())
    }
}

