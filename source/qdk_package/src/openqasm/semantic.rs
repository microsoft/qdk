macro_rules! register_semantic_nodes {
    ($m:ident; $($ty:ty),* $(,)?) => {
        $($m.add_class::<$ty>()?;)*
    };
}

/// Registers all semantic node classes with the given module.
///
/// The classes are registered into the `qdk._native._semantic` submodule (not
/// the flat `qdk._native` module), so their clean, un-prefixed Python names do
/// not collide with the syntax layer.
pub(crate) fn register_semantic_nodes(m: &Bound<'_, PyModule>) -> PyResult<()> {
    register_semantic_nodes!(m;
        SemType,
        SemSymbol,
        SemSymbolTable,
        SemExpr,
        SemStmt,
        SemProgram,
        SemHardwareQubit,
        SemQuantumGateModifier,
        SemRangeDefinition,
        SemDiscreteSet,
        SemSwitchCase,
        SemSubroutineParameter,
        SemErrExpr,
        SemResolvedIdent,
        SemCapturedResolvedIdent,
        SemUnaryOpExpr,
        SemBinaryOpExpr,
        SemLiteral,
        SemFunctionCall,
        SemBuiltinFunctionCall,
        SemCast,
        SemIndexedExpr,
        SemParen,
        SemMeasure,
        SemRuntimeSizeof,
        SemEvaluatedDurationof,
        SemConcat,
        SemAliasDecl,
        SemAssign,
        SemBarrier,
        SemBox,
        SemBlock,
        SemBreak,
        SemCalibration,
        SemCalibrationGrammar,
        SemClassicalDecl,
        SemContinue,
        SemDef,
        SemDefCal,
        SemDelay,
        SemEnd,
        SemExprStmt,
        SemExternDecl,
        SemForLoop,
        SemGateCall,
        SemIfStmt,
        SemInclude,
        SemIndexedAssign,
        SemInputDeclaration,
        SemOutputDeclaration,
        SemMeasureArrow,
        SemPragma,
        SemGateDefinition,
        SemQubitDecl,
        SemQubitArrayDecl,
        SemReset,
        SemReturn,
        SemSwitch,
        SemWhileLoop,
        SemErrStmt,
    );
    Ok(())
}

