/// Builds the semantic [`SemProgram`] root from an analyzed program.
pub(crate) fn build_program(
    py: Python<'_>,
    program: &sem::Program,
    symbols: &SymbolTable,
) -> PyResult<Py<SemProgram>> {
    let mut statements = Vec::with_capacity(program.statements.len());
    for stmt in &program.statements {
        statements.push(build_stmt(py, stmt, symbols)?);
    }
    let mut pragmas = Vec::with_capacity(program.pragmas.len());
    for pragma in &program.pragmas {
        pragmas.push(build_pragma(py, pragma)?);
    }
    let version = program.version.map(|version| version.to_string());
    let init = PyClassInitializer::from(QASMNode {
        span: Span { lo: 0, hi: 0 },
    })
    .add_subclass(SemProgram {
        version,
        pragmas,
        statements,
    });
    Py::new(py, init)
}

fn build_pragma(py: Python<'_>, pragma: &sem::Pragma) -> PyResult<Py<PyAny>> {
    let name = pragma.identifier.as_ref().map(PathKind::as_string);
    let value = pragma.value.as_ref().map(ToString::to_string);
    let init = SemPragma::init(Span::from(pragma.span), Vec::new(), name, value);
    Ok(Py::new(py, init)?.into_any())
}

#[allow(clippy::too_many_lines)]
fn build_stmt(py: Python<'_>, stmt: &sem::Stmt, symbols: &SymbolTable) -> PyResult<Py<PyAny>> {
    let span = Span::from(stmt.span);
    let annotations: Vec<String> = stmt
        .annotations
        .iter()
        .map(|annotation| annotation.identifier.as_string())
        .collect();

    let obj: Py<PyAny> = match stmt.kind.as_ref() {
        StmtKind::Alias(s) => {
            let exprs = expr_list(py, &s.exprs, symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(py, SemAliasDecl::init(span, annotations, name, exprs))?.into_any()
        }
        StmtKind::Assign(s) => {
            let lhs = build_expr(py, &s.lhs, symbols)?;
            let rhs = build_expr(py, &s.rhs, symbols)?;
            Py::new(py, SemAssign::init(span, annotations, lhs, rhs))?.into_any()
        }
        StmtKind::Barrier(s) => {
            let qubits = gate_operand_list(py, &s.qubits, symbols)?;
            Py::new(py, SemBarrier::init(span, annotations, qubits))?.into_any()
        }
        StmtKind::Box(s) => {
            let duration = build_opt_expr(py, s.duration.as_deref(), symbols)?;
            let body = stmt_list(py, &s.body, symbols)?;
            Py::new(py, SemBox::init(span, annotations, duration, body))?.into_any()
        }
        StmtKind::Block(s) => {
            let statements = stmt_list(py, &s.stmts, symbols)?;
            Py::new(py, SemBlock::init(span, annotations, statements))?.into_any()
        }
        StmtKind::Break(_) => Py::new(py, SemBreak::init(span, annotations))?.into_any(),
        StmtKind::Calibration(s) => Py::new(
            py,
            SemCalibration::init(span, annotations, s.content.to_string()),
        )?
        .into_any(),
        StmtKind::CalibrationGrammar(s) => Py::new(
            py,
            SemCalibrationGrammar::init(span, annotations, s.name.to_string()),
        )?
        .into_any(),
        StmtKind::ClassicalDecl(s) => {
            let ty_exprs = expr_list(py, &s.ty_exprs, symbols)?;
            let init_expr = build_expr(py, &s.init_expr, symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(
                py,
                SemClassicalDecl::init(span, annotations, name, ty_exprs, init_expr),
            )?
            .into_any()
        }
        StmtKind::Continue(_) => Py::new(py, SemContinue::init(span, annotations))?.into_any(),
        StmtKind::Def(s) => {
            let mut param_types = Vec::new();
            for param in &s.params {
                for ty_expr in &param.ty_exprs {
                    param_types.push(build_expr(py, ty_expr, symbols)?);
                }
            }
            let return_types = expr_list(py, &s.return_ty_exprs, symbols)?;
            let body = stmt_list(py, &s.body.stmts, symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(
                py,
                SemDef::init(span, annotations, name, param_types, return_types, body),
            )?
            .into_any()
        }
        StmtKind::DefCal(s) => Py::new(
            py,
            SemDefCal::init(span, annotations, s.content.to_string()),
        )?
        .into_any(),
        StmtKind::Delay(s) => {
            let duration = build_expr(py, &s.duration, symbols)?;
            let qubits = gate_operand_list(py, &s.qubits, symbols)?;
            Py::new(py, SemDelay::init(span, annotations, duration, qubits))?.into_any()
        }
        StmtKind::End(_) => Py::new(py, SemEnd::init(span, annotations))?.into_any(),
        StmtKind::ExprStmt(s) => {
            let expr = build_expr(py, &s.expr, symbols)?;
            Py::new(py, SemExprStmt::init(span, annotations, expr))?.into_any()
        }
        StmtKind::ExternDecl(s) => {
            let ty_exprs = expr_list(py, &s.ty_exprs, symbols)?;
            let return_types = expr_list(py, &s.return_ty_exprs, symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(
                py,
                SemExternDecl::init(span, annotations, name, ty_exprs, return_types),
            )?
            .into_any()
        }
        StmtKind::For(s) => {
            let ty_exprs = expr_list(py, &s.ty_exprs, symbols)?;
            let mut iterable = Vec::new();
            collect_enumerable_set(py, &mut iterable, &s.set_declaration, symbols)?;
            let body = build_stmt(py, &s.body, symbols)?;
            let name = symbol_name(symbols, s.loop_variable);
            Py::new(
                py,
                SemForLoop::init(span, annotations, name, ty_exprs, iterable, body),
            )?
            .into_any()
        }
        StmtKind::GateCall(s) => {
            let mut modifiers = Vec::new();
            for modifier in &s.modifiers {
                collect_modifier(py, &mut modifiers, modifier, symbols)?;
            }
            let args = expr_list(py, &s.args, symbols)?;
            let qubits = gate_operand_list(py, &s.qubits, symbols)?;
            let duration = build_opt_expr(py, s.duration.as_deref(), symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(
                py,
                SemGateCall::init(span, annotations, name, modifiers, args, qubits, duration),
            )?
            .into_any()
        }
        StmtKind::If(s) => {
            let condition = build_expr(py, &s.condition, symbols)?;
            let then_block = build_stmt(py, &s.if_body, symbols)?;
            let else_block = match &s.else_body {
                Some(else_body) => Some(build_stmt(py, else_body, symbols)?),
                None => None,
            };
            Py::new(
                py,
                SemIfStmt::init(span, annotations, condition, then_block, else_block),
            )?
            .into_any()
        }
        StmtKind::Include(s) => Py::new(
            py,
            SemInclude::init(span, annotations, s.filename.to_string()),
        )?
        .into_any(),
        StmtKind::IndexedAssign(s) => {
            let lhs = build_expr(py, &s.lhs, symbols)?;
            let mut indices = Vec::new();
            for index in &s.indices {
                collect_index(py, &mut indices, index, symbols)?;
            }
            let rhs = build_expr(py, &s.rhs, symbols)?;
            Py::new(
                py,
                SemIndexedAssign::init(span, annotations, lhs, indices, rhs),
            )?
            .into_any()
        }
        StmtKind::InputDeclaration(s) => {
            let ty_exprs = expr_list(py, &s.ty_exprs, symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(
                py,
                SemInputDeclaration::init(span, annotations, name, ty_exprs),
            )?
            .into_any()
        }
        StmtKind::OutputDeclaration(s) => {
            let ty_exprs = expr_list(py, &s.ty_exprs, symbols)?;
            let init_expr = build_expr(py, &s.init_expr, symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(
                py,
                SemOutputDeclaration::init(span, annotations, name, ty_exprs, init_expr),
            )?
            .into_any()
        }
        StmtKind::MeasureArrow(s) => {
            let mut qubits = Vec::new();
            collect_gate_operand(py, &mut qubits, &s.measurement.operand, symbols)?;
            let target = match &s.target {
                Some(target) => Some(build_expr(py, target, symbols)?),
                None => None,
            };
            Py::new(py, SemMeasureArrow::init(span, annotations, qubits, target))?.into_any()
        }
        StmtKind::Pragma(s) => {
            let name = s.identifier.as_ref().map(PathKind::as_string);
            let value = s.value.as_ref().map(ToString::to_string);
            Py::new(py, SemPragma::init(span, annotations, name, value))?.into_any()
        }
        StmtKind::QuantumGateDefinition(s) => {
            let params = s
                .params
                .iter()
                .map(|id| symbol_name(symbols, *id).unwrap_or_default())
                .collect();
            let qubits = s
                .qubits
                .iter()
                .map(|id| symbol_name(symbols, *id).unwrap_or_default())
                .collect();
            let body = stmt_list(py, &s.body.stmts, symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(
                py,
                SemGateDefinition::init(span, annotations, name, params, qubits, body),
            )?
            .into_any()
        }
        StmtKind::QubitDecl(s) => {
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(py, SemQubitDecl::init(span, annotations, name))?.into_any()
        }
        StmtKind::QubitArrayDecl(s) => {
            let size = build_expr(py, &s.size, symbols)?;
            let name = symbol_name(symbols, s.symbol_id);
            Py::new(py, SemQubitArrayDecl::init(span, annotations, name, size))?.into_any()
        }
        StmtKind::Reset(s) => {
            let mut qubits = Vec::new();
            collect_gate_operand(py, &mut qubits, &s.operand, symbols)?;
            Py::new(py, SemReset::init(span, annotations, qubits))?.into_any()
        }
        StmtKind::Return(s) => {
            let value = build_opt_expr(py, s.expr.as_deref(), symbols)?;
            Py::new(py, SemReturn::init(span, annotations, value))?.into_any()
        }
        StmtKind::Switch(s) => {
            let target = build_expr(py, &s.target, symbols)?;
            let mut labels = Vec::new();
            let mut body = Vec::new();
            for case in &s.cases {
                for label in &case.labels {
                    labels.push(build_expr(py, label, symbols)?);
                }
                for case_stmt in &case.block.stmts {
                    body.push(build_stmt(py, case_stmt, symbols)?);
                }
            }
            let mut default = Vec::new();
            if let Some(default_block) = &s.default {
                for default_stmt in &default_block.stmts {
                    default.push(build_stmt(py, default_stmt, symbols)?);
                }
            }
            Py::new(
                py,
                SemSwitch::init(span, annotations, target, labels, body, default),
            )?
            .into_any()
        }
        StmtKind::WhileLoop(s) => {
            let condition = build_expr(py, &s.condition, symbols)?;
            let body = build_stmt(py, &s.body, symbols)?;
            Py::new(py, SemWhileLoop::init(span, annotations, condition, body))?.into_any()
        }
        StmtKind::Err => Py::new(py, SemErrStmt::init(span, annotations))?.into_any(),
    };
    Ok(obj)
}

#[allow(clippy::too_many_lines)]
fn build_expr(py: Python<'_>, expr: &sem::Expr, symbols: &SymbolTable) -> PyResult<Py<PyAny>> {
    let span = Span::from(expr.span);
    let ty = SemType::from_core(py, &expr.ty)?;
    let const_value = literal_value(py, expr.const_value.as_ref())?;

    let obj: Py<PyAny> = match expr.kind.as_ref() {
        ExprKind::Err => Py::new(py, SemErrExpr::init(span, ty, const_value, None))?.into_any(),
        ExprKind::ResolvedIdent(id) => {
            let symbol = Some(build_symbol(py, symbols, *id)?);
            let name = symbol_name(symbols, *id);
            Py::new(
                py,
                SemResolvedIdent::init(span, ty, const_value, symbol, name, (*id).into()),
            )?
            .into_any()
        }
        ExprKind::CapturedResolvedIdent(id) => {
            let symbol = Some(build_symbol(py, symbols, *id)?);
            let name = symbol_name(symbols, *id);
            Py::new(
                py,
                SemCapturedResolvedIdent::init(span, ty, const_value, symbol, name, (*id).into()),
            )?
            .into_any()
        }
        ExprKind::UnaryOp(e) => {
            let operand = build_expr(py, &e.expr, symbols)?;
            Py::new(
                py,
                SemUnaryOpExpr::init(span, ty, const_value, None, e.op.to_string(), operand),
            )?
            .into_any()
        }
        ExprKind::BinaryOp(e) => {
            let lhs = build_expr(py, &e.lhs, symbols)?;
            let rhs = build_expr(py, &e.rhs, symbols)?;
            Py::new(
                py,
                SemBinaryOpExpr::init(span, ty, const_value, None, e.op.to_string(), lhs, rhs),
            )?
            .into_any()
        }
        ExprKind::Lit(lit) => {
            let (value, elements) = literal_expr_parts(py, lit, symbols)?;
            Py::new(
                py,
                SemLiteral::init(span, ty, const_value, None, value, elements),
            )?
            .into_any()
        }
        ExprKind::ResolvedFunctionCall(e) => {
            let args = expr_list(py, &e.args, symbols)?;
            let symbol = Some(build_symbol(py, symbols, e.callee_id)?);
            let name = symbol_name(symbols, e.callee_id);
            Py::new(
                py,
                SemFunctionCall::init(
                    span,
                    ty,
                    const_value,
                    symbol,
                    name,
                    e.callee_id.into(),
                    args,
                ),
            )?
            .into_any()
        }
        ExprKind::BuiltinFunctionCall(e) => {
            let args = expr_list(py, &e.args, symbols)?;
            Py::new(
                py,
                SemBuiltinFunctionCall::init(span, ty, const_value, None, e.name.to_string(), args),
            )?
            .into_any()
        }
        ExprKind::Cast(e) => {
            let operand = build_expr(py, &e.expr, symbols)?;
            let ty_exprs = expr_list(py, &e.ty_exprs, symbols)?;
            Py::new(
                py,
                SemCast::init(
                    span,
                    ty,
                    const_value,
                    None,
                    operand,
                    ty_exprs,
                    e.kind.to_string(),
                ),
            )?
            .into_any()
        }
        ExprKind::IndexedExpr(e) => {
            let collection = build_expr(py, &e.collection, symbols)?;
            let mut indices = Vec::new();
            collect_index(py, &mut indices, &e.index, symbols)?;
            Py::new(
                py,
                SemIndexedExpr::init(span, ty, const_value, None, collection, indices),
            )?
            .into_any()
        }
        ExprKind::Paren(inner) => {
            let operand = build_expr(py, inner, symbols)?;
            Py::new(py, SemParen::init(span, ty, const_value, None, operand))?.into_any()
        }
        ExprKind::Measure(e) => {
            let mut qubits = Vec::new();
            collect_gate_operand(py, &mut qubits, &e.operand, symbols)?;
            Py::new(py, SemMeasure::init(span, ty, const_value, None, qubits))?.into_any()
        }
        ExprKind::RuntimeSizeof(e) => {
            let array = build_expr(py, &e.array, symbols)?;
            let dimension = build_expr(py, &e.dimension, symbols)?;
            Py::new(
                py,
                SemRuntimeSizeof::init(span, ty, const_value, None, array, dimension, e.array_rank),
            )?
            .into_any()
        }
        ExprKind::EvaluatedDurationof(e) => {
            let body = stmt_list(py, &e.scope.stmts, symbols)?;
            Py::new(
                py,
                SemEvaluatedDurationof::init(span, ty, const_value, None, body),
            )?
            .into_any()
        }
        ExprKind::Concat(e) => {
            let operands = expr_list(py, &e.operands, symbols)?;
            Py::new(py, SemConcat::init(span, ty, const_value, None, operands))?.into_any()
        }
    };
    Ok(obj)
}

// ----------------------------------------------------------------------------
// Child-collection helpers
// ----------------------------------------------------------------------------

fn expr_list(
    py: Python<'_>,
    exprs: &[sem::Expr],
    symbols: &SymbolTable,
) -> PyResult<Vec<Py<PyAny>>> {
    let mut out = Vec::with_capacity(exprs.len());
    for expr in exprs {
        out.push(build_expr(py, expr, symbols)?);
    }
    Ok(out)
}

fn stmt_list(
    py: Python<'_>,
    stmts: &[sem::Stmt],
    symbols: &SymbolTable,
) -> PyResult<Vec<Py<PyAny>>> {
    let mut out = Vec::with_capacity(stmts.len());
    for stmt in stmts {
        out.push(build_stmt(py, stmt, symbols)?);
    }
    Ok(out)
}

fn build_opt_expr(
    py: Python<'_>,
    expr: Option<&sem::Expr>,
    symbols: &SymbolTable,
) -> PyResult<Option<Py<PyAny>>> {
    match expr {
        Some(expr) => Ok(Some(build_expr(py, expr, symbols)?)),
        None => Ok(None),
    }
}

fn gate_operand_list(
    py: Python<'_>,
    operands: &[sem::GateOperand],
    symbols: &SymbolTable,
) -> PyResult<Vec<Py<PyAny>>> {
    let mut out = Vec::with_capacity(operands.len());
    for operand in operands {
        collect_gate_operand(py, &mut out, operand, symbols)?;
    }
    Ok(out)
}

fn collect_gate_operand(
    py: Python<'_>,
    out: &mut Vec<Py<PyAny>>,
    operand: &sem::GateOperand,
    symbols: &SymbolTable,
) -> PyResult<()> {
    match &operand.kind {
        sem::GateOperandKind::Expr(expr) => out.push(build_expr(py, expr, symbols)?),
        sem::GateOperandKind::HardwareQubit(hw) => out.push(build_hardware_qubit(py, hw)?),
        sem::GateOperandKind::Err => {}
    }
    Ok(())
}

fn build_hardware_qubit(py: Python<'_>, hw: &sem::HardwareQubit) -> PyResult<Py<PyAny>> {
    let init = PyClassInitializer::from(QASMNode {
        span: Span::from(hw.span),
    })
    .add_subclass(Expression)
    .add_subclass(SemHardwareQubit {
        name: hw.name.to_string(),
    });
    Ok(Py::new(py, init)?.into_any())
}

fn collect_index(
    py: Python<'_>,
    out: &mut Vec<Py<PyAny>>,
    index: &sem::Index,
    symbols: &SymbolTable,
) -> PyResult<()> {
    match index {
        sem::Index::Expr(expr) => out.push(build_expr(py, expr, symbols)?),
        sem::Index::Range(range) => collect_range(py, out, range, symbols)?,
    }
    Ok(())
}

fn collect_range(
    py: Python<'_>,
    out: &mut Vec<Py<PyAny>>,
    range: &sem::Range,
    symbols: &SymbolTable,
) -> PyResult<()> {
    if let Some(start) = &range.start {
        out.push(build_expr(py, start, symbols)?);
    }
    if let Some(step) = &range.step {
        out.push(build_expr(py, step, symbols)?);
    }
    if let Some(end) = &range.end {
        out.push(build_expr(py, end, symbols)?);
    }
    Ok(())
}

fn collect_enumerable_set(
    py: Python<'_>,
    out: &mut Vec<Py<PyAny>>,
    set: &sem::EnumerableSet,
    symbols: &SymbolTable,
) -> PyResult<()> {
    match set {
        sem::EnumerableSet::Set(set) => {
            for value in &set.values {
                out.push(build_expr(py, value, symbols)?);
            }
        }
        sem::EnumerableSet::Range(range) => collect_range(py, out, range, symbols)?,
        sem::EnumerableSet::Expr(expr) => out.push(build_expr(py, expr, symbols)?),
    }
    Ok(())
}

fn collect_modifier(
    py: Python<'_>,
    out: &mut Vec<Py<PyAny>>,
    modifier: &sem::QuantumGateModifier,
    symbols: &SymbolTable,
) -> PyResult<()> {
    match &modifier.kind {
        sem::GateModifierKind::Inv => {}
        sem::GateModifierKind::Pow(expr)
        | sem::GateModifierKind::Ctrl(expr)
        | sem::GateModifierKind::NegCtrl(expr) => out.push(build_expr(py, expr, symbols)?),
    }
    Ok(())
}

