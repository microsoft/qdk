/// The root of a semantic `OpenQASM` program.
#[pyclass(extends = QASMNode, frozen, name = "Program", module = "qdk._native._semantic")]
pub(crate) struct SemProgram {
    /// The declared `OpenQASM` version, if any (for example `"3.0"`).
    #[pyo3(get)]
    version: Option<String>,
    /// The program's top-level pragmas, in source order.
    #[pyo3(get)]
    pragmas: Vec<Py<PyAny>>,
    /// The program's top-level statements, in source order.
    #[pyo3(get)]
    statements: Vec<Py<PyAny>>,
}

#[pymethods]
impl SemProgram {
    /// The program's child nodes: pragmas followed by statements.
    fn children(&self, py: Python<'_>) -> Vec<Py<PyAny>> {
        let mut out = Vec::with_capacity(self.pragmas.len() + self.statements.len());
        out.extend(self.pragmas.iter().map(|node| node.clone_ref(py)));
        out.extend(self.statements.iter().map(|node| node.clone_ref(py)));
        out
    }

    fn __repr__(&self) -> String {
        format!("Program(statements=[{} items])", self.statements.len())
    }
}

/// A hardware-qubit gate operand (for example `$0`).
///
/// This extends [`Expression`] rather than [`SemExpr`] because a hardware qubit
/// carries no resolved type or const value; it is a physical qubit reference.
///
/// The Rust identifier stays `SemHardwareQubit`, but the class is presented to
/// Python as `HardwareQubit` in the `qdk._native._semantic` submodule so the
/// semantic family can use clean, un-prefixed names without colliding with the
/// syntax layer in `qdk._native`.
#[pyclass(
    extends = Expression,
    frozen,
    name = "HardwareQubit",
    module = "qdk._native._semantic"
)]
pub(crate) struct SemHardwareQubit {
    /// The hardware qubit's identifier text (for example `"$0"`).
    #[pyo3(get)]
    name: String,
}

#[pymethods]
impl SemHardwareQubit {
    #[allow(clippy::unused_self)]
    fn children(&self) -> Vec<Py<PyAny>> {
        Vec::new()
    }

    fn __repr__(&self) -> String {
        format!("HardwareQubit(name={:?})", self.name)
    }
}

