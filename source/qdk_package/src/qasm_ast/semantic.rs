/// The abstract base of every semantic expression node.
///
/// This class has no Python constructor; it exists for `isinstance` dispatch and
/// to carry the resolved `ty`, `const_value`, and (for resolved identifiers)
/// `symbol` shared by all semantic expressions.
#[pyclass(extends = Expression, subclass, frozen, name = "Expr", module = "qdk._native._semantic")]
pub(crate) struct SemExpr {
    /// The resolved type of the expression.
    #[pyo3(get)]
    ty: Py<SemType>,
    /// The const-evaluated value of the expression, if any.
    #[pyo3(get)]
    const_value: Option<Py<PyAny>>,
    /// The resolved symbol for identifier expressions, if any.
    #[pyo3(get)]
    symbol: Option<Py<SemSymbol>>,
}

/// The abstract base of every semantic statement node.
///
/// This class has no Python constructor; it exists for `isinstance` dispatch and
/// to carry the `annotations` shared by all semantic statements.
#[pyclass(extends = Statement, subclass, frozen, name = "Stmt", module = "qdk._native._semantic")]
pub(crate) struct SemStmt {
    /// The statement's annotation identifiers, in source order.
    #[pyo3(get)]
    annotations: Vec<String>,
}

/// Builds the initializer chain `QASMNode -> Expression -> SemExpr`.
fn sem_expr_base(
    span: Span,
    ty: Py<SemType>,
    const_value: Option<Py<PyAny>>,
    symbol: Option<Py<SemSymbol>>,
) -> PyClassInitializer<SemExpr> {
    PyClassInitializer::from(QASMNode { span })
        .add_subclass(Expression)
        .add_subclass(SemExpr {
            ty,
            const_value,
            symbol,
        })
}

/// Builds the initializer chain `QASMNode -> Statement -> SemStmt`.
fn sem_stmt_base(span: Span, annotations: Vec<String>) -> PyClassInitializer<SemStmt> {
    PyClassInitializer::from(QASMNode { span })
        .add_subclass(Statement)
        .add_subclass(SemStmt { annotations })
}

