/// An opaque, read-only view of a resolved semantic type.
///
/// The `name` is the type's textual rendering (for example `"int[32]"` or
/// `"qubit"`), `is_const` reflects whether the type is compile-time constant,
/// and `width` is the type's bit width when it has one.
#[pyclass(name = "Type", module = "qdk._native._semantic", frozen)]
pub(crate) struct SemType {
    #[pyo3(get)]
    name: String,
    #[pyo3(get)]
    is_const: bool,
    #[pyo3(get)]
    width: Option<u32>,
}

impl SemType {
    fn from_core(py: Python<'_>, ty: &CoreType) -> PyResult<Py<SemType>> {
        Py::new(
            py,
            SemType {
                name: ty.to_string(),
                is_const: ty.is_const(),
                width: ty.width(),
            },
        )
    }
}

#[pymethods]
impl SemType {
    fn __str__(&self) -> String {
        self.name.clone()
    }

    fn __repr__(&self) -> String {
        format!("Type({:?}, is_const={})", self.name, self.is_const)
    }
}

/// A read-only view of a resolved symbol.
#[pyclass(name = "Symbol", module = "qdk._native._semantic", frozen)]
pub(crate) struct SemSymbol {
    /// The symbol's unique id within the [`SemSymbolTable`].
    #[pyo3(get)]
    id: u32,
    /// The symbol's name.
    #[pyo3(get)]
    name: String,
    /// The span where the symbol is declared.
    #[pyo3(get)]
    span: Span,
    /// The symbol's resolved type.
    #[pyo3(get)]
    ty: Py<SemType>,
    /// The symbol's I/O kind (`"Default"`, `"Input"`, or `"Output"`).
    #[pyo3(get)]
    io_kind: String,
    /// The symbol's const-evaluated value, if it is a constant.
    #[pyo3(get)]
    const_value: Option<Py<PyAny>>,
}

#[pymethods]
impl SemSymbol {
    fn __repr__(&self) -> String {
        format!("Symbol(id={}, name={:?})", self.id, self.name)
    }
}

/// An iterable, read-only projection of the resolved symbol table.
#[pyclass(name = "SymbolTable", module = "qdk._native._semantic", frozen)]
pub(crate) struct SemSymbolTable {
    symbols: Vec<Py<SemSymbol>>,
}

#[pymethods]
impl SemSymbolTable {
    /// The number of symbols in the table.
    fn __len__(&self) -> usize {
        self.symbols.len()
    }

    /// Iterates over the [`SemSymbol`] views in the table.
    fn __iter__(&self, py: Python<'_>) -> PyResult<Py<PyAny>> {
        let list = PyList::new(py, self.symbols.iter().map(|symbol| symbol.clone_ref(py)))?;
        Ok(list.as_any().try_iter()?.into_any().unbind())
    }

    /// Returns the symbol with the given id, or `None`.
    fn get(&self, py: Python<'_>, id: u32) -> Option<Py<SemSymbol>> {
        self.symbols
            .iter()
            .find(|symbol| symbol.borrow(py).id == id)
            .map(|symbol| symbol.clone_ref(py))
    }

    /// Returns the first symbol with the given name, or `None`.
    fn lookup(&self, py: Python<'_>, name: &str) -> Option<Py<SemSymbol>> {
        self.symbols
            .iter()
            .find(|symbol| symbol.borrow(py).name == name)
            .map(|symbol| symbol.clone_ref(py))
    }

    /// All [`SemSymbol`] views in the table, in id order.
    fn symbols(&self, py: Python<'_>) -> Vec<Py<SemSymbol>> {
        self.symbols
            .iter()
            .map(|symbol| symbol.clone_ref(py))
            .collect()
    }

    fn __repr__(&self) -> String {
        format!("SymbolTable([{} symbols])", self.symbols.len())
    }
}

fn build_sem_symbol(py: Python<'_>, id: u32, symbol: &Symbol) -> PyResult<Py<SemSymbol>> {
    Py::new(
        py,
        SemSymbol {
            id,
            name: symbol.name.clone(),
            span: symbol.span.into(),
            ty: SemType::from_core(py, &symbol.ty)?,
            io_kind: symbol.io_kind.to_string(),
            const_value: literal_value(py, symbol.get_const_value().as_ref())?,
        },
    )
}

/// Builds the iterable [`SemSymbolTable`] projection from the resolved table.
///
/// Symbol ids are assigned sequentially as symbols are inserted, and the
/// underlying storage never leaves gaps, so a symbol's insertion index equals
/// its id.
pub(crate) fn build_symbol_table(
    py: Python<'_>,
    symbols: &SymbolTable,
) -> PyResult<Py<SemSymbolTable>> {
    let mut entries = Vec::new();
    for (index, symbol) in symbols.symbols().enumerate() {
        let id = u32::try_from(index).expect("symbol id should fit into u32");
        entries.push(build_sem_symbol(py, id, symbol.as_ref())?);
    }
    Py::new(py, SemSymbolTable { symbols: entries })
}

fn build_symbol(py: Python<'_>, symbols: &SymbolTable, id: SymbolId) -> PyResult<Py<SemSymbol>> {
    build_sem_symbol(py, id.into(), symbols[id].as_ref())
}

fn symbol_name(symbols: &SymbolTable, id: SymbolId) -> Option<String> {
    Some(symbols[id].name.clone())
}

