//! Rich, read-only Python projections of the semantic `OpenQASM` AST.
//!
//! Where [`super::nodes`] projects the *syntactic* tree, this module projects
//! the *semantic* tree produced by [`qdk_openqasm::analyze_source`]. The
//! semantic tree carries resolved type and const-value information. Each class
//! keeps a `Sem`-prefixed Rust identifier but is presented to Python under a
//! clean, un-prefixed name inside the `qdk._native._semantic` submodule (for
//! example Rust `SemGateCall` -> Python `GateCall`). Isolating the family in a
//! submodule lets it use the un-prefixed names without colliding with the
//! syntax layer's `openqasm3`-parity names in the flat `qdk._native` module.
//!
//! * [`SemExpr`] (Python `Expr`) is the base of every semantic expression. It
//!   extends [`Expression`] (and therefore `QASMNode`) and adds `ty`,
//!   `const_value`, and `symbol` accessors.
//! * [`SemStmt`] (Python `Stmt`) is the base of every semantic statement. It
//!   extends [`Statement`] (and therefore `QASMNode`) and adds an `annotations`
//!   accessor.
//! * [`SemProgram`] (Python `Program`) is the root of a semantic program.
//!
//! Concrete per-variant leaf classes are generated by the [`qasm_node!`] macro
//! and expose *named*, typed accessors for their children (for example
//! `SemBinaryOpExpr.lhs` / `.rhs` / `.op`) plus a uniform `children()` method
//! for generic traversal. There is intentionally no `kind` discriminant;
//! callers dispatch via `isinstance` and `type(node).__name__`.
//!
//! The resolved [`SymbolTable`] is projected as [`SemSymbolTable`], an iterable
//! collection of [`SemSymbol`] views, and types are projected as opaque
//! [`SemType`] values.
//!
//! All projections are eagerly materialized into owned, frozen values (scalars
//! plus `Py<PyAny>` references to already-built children), so they are
//! `Send + Sync` and hold no borrow into the Rust analysis result.
//!
//! ## Semantic name to variant table
//!
//! The mapping below is the single source of truth for the semantic AST
//! variants, their `Sem`-prefixed Rust identifiers, and the clean Python names
//! they present in the `qdk._native._semantic` submodule. It is kept in sync
//! with `qdk_openqasm::semantic::ast`.
//!
//! Statements (`StmtKind` -> Rust ident -> Python name):
//! * `Alias` -> `SemAliasDecl` -> `AliasDecl`
//! * `Assign` -> `SemAssign` -> `Assign`
//! * `Barrier` -> `SemBarrier` -> `Barrier`
//! * `Box` -> `SemBox` -> `Box`
//! * `Block` -> `SemBlock` -> `Block`
//! * `Break` -> `SemBreak` -> `Break`
//! * `Calibration` -> `SemCalibration` -> `Calibration`
//! * `CalibrationGrammar` -> `SemCalibrationGrammar` -> `CalibrationGrammar`
//! * `ClassicalDecl` -> `SemClassicalDecl` -> `ClassicalDecl`
//! * `Continue` -> `SemContinue` -> `Continue`
//! * `Def` -> `SemDef` -> `Def`
//! * `DefCal` -> `SemDefCal` -> `DefCal`
//! * `Delay` -> `SemDelay` -> `Delay`
//! * `End` -> `SemEnd` -> `End`
//! * `ExprStmt` -> `SemExprStmt` -> `ExprStmt`
//! * `ExternDecl` -> `SemExternDecl` -> `ExternDecl`
//! * `For` -> `SemForLoop` -> `ForLoop`
//! * `GateCall` -> `SemGateCall` -> `GateCall`
//! * `If` -> `SemIfStmt` -> `IfStmt`
//! * `Include` -> `SemInclude` -> `Include`
//! * `IndexedAssign` -> `SemIndexedAssign` -> `IndexedAssign`
//! * `InputDeclaration` -> `SemInputDeclaration` -> `InputDeclaration`
//! * `OutputDeclaration` -> `SemOutputDeclaration` -> `OutputDeclaration`
//! * `MeasureArrow` -> `SemMeasureArrow` -> `MeasureArrow`
//! * `Pragma` -> `SemPragma` -> `Pragma`
//! * `QuantumGateDefinition` -> `SemGateDefinition` -> `GateDefinition`
//! * `QubitDecl` -> `SemQubitDecl` -> `QubitDecl`
//! * `QubitArrayDecl` -> `SemQubitArrayDecl` -> `QubitArrayDecl`
//! * `Reset` -> `SemReset` -> `Reset`
//! * `Return` -> `SemReturn` -> `Return`
//! * `Switch` -> `SemSwitch` -> `Switch`
//! * `WhileLoop` -> `SemWhileLoop` -> `WhileLoop`
//! * `Err` -> `SemErrStmt` -> `ErrStmt`
//!
//! Expressions (`ExprKind` -> Rust ident -> Python name):
//! * `Err` -> `SemErrExpr` -> `ErrExpr`
//! * `ResolvedIdent` -> `SemResolvedIdent` -> `ResolvedIdent`
//! * `CapturedResolvedIdent` -> `SemCapturedResolvedIdent` -> `CapturedResolvedIdent`
//! * `UnaryOp` -> `SemUnaryOpExpr` -> `UnaryOpExpr`
//! * `BinaryOp` -> `SemBinaryOpExpr` -> `BinaryOpExpr`
//! * `Lit` -> `SemLiteral` -> `Literal`
//! * `ResolvedFunctionCall` -> `SemFunctionCall` -> `FunctionCall`
//! * `BuiltinFunctionCall` -> `SemBuiltinFunctionCall` -> `BuiltinFunctionCall`
//! * `Cast` -> `SemCast` -> `Cast`
//! * `IndexedExpr` -> `SemIndexedExpr` -> `IndexedExpr`
//! * `Paren` -> `SemParen` -> `Paren`
//! * `Measure` -> `SemMeasure` -> `Measure`
//! * `RuntimeSizeof` -> `SemRuntimeSizeof` -> `RuntimeSizeof`
//! * `EvaluatedDurationof` -> `SemEvaluatedDurationof` -> `EvaluatedDurationof`
//! * `Concat` -> `SemConcat` -> `Concat`
//!
//! The shared category bases and projections are `SemExpr` -> `Expr`,
//! `SemStmt` -> `Stmt`, `SemProgram` -> `Program`, `SemType` -> `Type`,
//! `SemSymbol` -> `Symbol`, and `SemSymbolTable` -> `SymbolTable`.
//!
//! Gate operands of the hardware-qubit form are projected as
//! [`SemHardwareQubit`] (Python `HardwareQubit`) so that no operand is silently
//! dropped.

